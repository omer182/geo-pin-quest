// Helper function to get cities for a specific level
export const getCitiesForLevel = (level: number): City[] => {
  return CITIES_BY_LEVEL[level] || CITIES_BY_LEVEL[1];
};

// Helper function to get random cities from a level
export const getRandomCitiesFromLevel = (level: number, count: number = 5): City[] => {
  const levelCities = getCitiesForLevel(level);
  const shuffled = [...levelCities].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, levelCities.length));
};
